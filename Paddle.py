from turtle import Turtle


class Paddles(Turtle):

    def __init__(self, coordinate):
        super().__init__()
        self.COORDINATES = coordinate
        self.shape("square")
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.color("white")
        self.penup()
        self.goto(self.COORDINATES)

    def go_up(self):
        new_y = self.ycor() + 20
        self.goto(x=self.xcor(), y=new_y)

    def go_down(self):
        new_y = self.ycor() - 20
        self.goto(x=self.xcor(), y=new_y)

